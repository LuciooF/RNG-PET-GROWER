-- Utility functions for working with pet names
local PetNamesModule = {}

-- Get total count of pets
function PetNamesModule.getTotalCount()
    return #PetNames
end

-- Get all pet names
function PetNamesModule.getAllNames()
    return PetNames
end

-- Check if a pet name exists
function PetNamesModule.exists(petName)
    for _, name in ipairs(PetNames) do
        if name == petName then
            return true
        end
    end
    return false
end

-- Get pet name by index
function PetNamesModule.getByIndex(index)
    return PetNames[index]
end

-- Find index of a pet name
function PetNamesModule.getIndex(petName)
    for i, name in ipairs(PetNames) do
        if name == petName then
            return i
        end
    end
    return nil
end

-- Get random pet name
function PetNamesModule.getRandom()
    local randomIndex = math.random(1, #PetNames)
    return PetNames[randomIndex]
end

return PetNamesModule